/*
  * Basic Operators
  */ 

let yearsJose = 15;
let yearsIgor = 21;
let yearNow   = 2021;


/*
 * Math Operator
 */
let yearJose = yearNow - yearsJose;
let yearIgor = yearNow - yearsIgor;

console.log(yearJose);
console.log(yearNow * 3);
console.log(yearNow / 3);

/*
 * Logical Operator
 */ 

let igorOlder = yearsIgor > yearsJose;
console.log(igorOlder);

/*
 * typeof operator
 */

console.log(typeof igorOlder);
console.log(typeof yearJose);













