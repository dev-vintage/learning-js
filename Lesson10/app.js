/*
 * Loops an iteration
 */

/*
 * for loop
 */

for (var i = 0; i < 10; i++){
	console.log(i);
}

let john = ['John', 'Smith', 1990, 'designer', false];

for (var i = 0; i < john.length; i++){
	console.log(john[i]);
}

/*
 * while loop
 */

var i = 0;

while(i < john.length){
	console.log(john[i]);
	i++;
}


